/// "Doğru mu Yanlış mı?" mini oyunu için veri modeli ve önerme havuzu.
///
/// Her [DogruYanlisOnerme] bir SORU değil bir İDDİA'dır; oyuncu kartı görür ve
/// "DOĞRU" ya da "YANLIŞ" der. Yanlış önermeler kasıtlı olarak İNANDIRICI
/// kurulmuştur: bariz saçmalıklar yerine bir yılı, bir bölgeyi, bir sayıyı ya
/// da bir kişiyi kaydıran ince hatalar içerirler (ör. Lozan'ın yılını 1923
/// yerine 1922 vermek gibi).
///
/// TÜM BİLGİLER GERÇEKTİR: doğru önermeler doğrulanmış bilgilerdir, yanlış
/// önermelerin [aciklama] alanında da doğrusu yazılıdır. Emin olunmayan hiçbir
/// tarih/sayı/coğrafi bilgi havuza alınmamıştır.
///
/// Havuzdaki oran kabaca yarı yarıyadır (bkz. [kDogruYanlisDogruSayisi] /
/// [kDogruYanlisYanlisSayisi]) — böylece "hepsine doğru de" stratejisi işe
/// yaramaz.
class DogruYanlisOnerme {
  /// Kartın üstünde yazan iddia.
  final String metin;

  /// İddia gerçekten doğru mu?
  final bool dogru;

  /// Ders/konu etiketi — kartın üstündeki çipte gösterilir.
  final String ders;

  /// Kısa açıklama; önerme yanlışsa doğrusunu söyler.
  final String aciklama;

  /// Zorluk = sınav türü seviyesi: 'ortaogretim' (kolay) | 'onlisans' (normal)
  /// | 'lisans' (zor). Boş '' ise TÜM seviyeler için uygundur (genel bilgi) ve
  /// kullanıcı hangi seviyeyi seçerse seçsin gösterilir. Filtreleme
  /// [seviyeyeGoreOnermeler] içinde yapılır; Question.examType ile aynı sözlük.
  final String seviye;

  const DogruYanlisOnerme({
    required this.metin,
    required this.dogru,
    required this.ders,
    required this.aciklama,
    this.seviye = '',
  });
}

/// Havuzdaki DOĞRU önerme sayısı (bilgi amaçlı, ekranda gösterilebilir).
int get kDogruYanlisDogruSayisi =>
    kDogruYanlisOnermeler.where((o) => o.dogru).length;

/// Havuzdaki YANLIŞ önerme sayısı.
int get kDogruYanlisYanlisSayisi =>
    kDogruYanlisOnermeler.where((o) => !o.dogru).length;

/// Kullanıcının seçtiği eğitim seviyesine ([StorageService.getExamType] →
/// 'ortaogretim' | 'onlisans' | 'lisans' | '') göre önerme havuzunu süzer.
///
/// KURAL (QuestionPicker ile aynı desen):
///  • Seçim boşsa ('') hiçbir filtre yok — tüm havuz döner.
///  • Aksi halde: seçilen seviyeye ait ([seviye]==hedef) + TÜM seviyeler için
///    uygun ([seviye]=='') önermeler döner.
///
/// "AZ KALIRSA GENİŞLET": süzülen havuz [enAz]'dan azsa, kullanıcının boş
/// ekranla karşılaşmaması için TÜM havuza düşülür. Böylece bir seviyede yeterli
/// içerik yoksa oyun yine de dolu bir desteyle oynanır (kullanıcının isteği).
List<DogruYanlisOnerme> seviyeyeGoreOnermeler(String seviye, {int enAz = 10}) {
  if (seviye.isEmpty) return kDogruYanlisOnermeler;
  final suzulmus = kDogruYanlisOnermeler
      .where((o) => o.seviye.isEmpty || o.seviye == seviye)
      .toList();
  if (suzulmus.length < enAz) return kDogruYanlisOnermeler;
  return suzulmus;
}

