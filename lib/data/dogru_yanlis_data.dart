/// "Doğru mu Yanlış mı?" mini oyunu için veri modeli ve önerme havuzu.
///
/// Her [DogruYanlisOnerme] bir SORU değil bir İDDİA'dır; oyuncu kartı görür ve
/// "DOĞRU" ya da "YANLIŞ" der. Yanlış önermeler kasıtlı olarak İNANDIRICI
/// kurulmuştur: bariz saçmalıklar yerine bir yılı, bir bölgeyi, bir sayıyı ya
/// da bir kişiyi kaydıran ince hatalar içerirler (ör. Lozan'ın yılını 1923
/// yerine 1922 vermek gibi).
///
/// TÜM BİLGİLER GERÇEKTİR: doğru önermeler doğrulanmış bilgilerdir, yanlış
/// önermelerin [aciklama] alanında da doğrusu yazılıdır. Emin olunmayan hiçbir
/// tarih/sayı/coğrafi bilgi havuza alınmamıştır.
///
/// Havuzdaki oran kabaca yarı yarıyadır (bkz. [kDogruYanlisDogruSayisi] /
/// [kDogruYanlisYanlisSayisi]) — böylece "hepsine doğru de" stratejisi işe
/// yaramaz.
class DogruYanlisOnerme {
  /// Kartın üstünde yazan iddia.
  final String metin;

  /// İddia gerçekten doğru mu?
  final bool dogru;

  /// Ders/konu etiketi — kartın üstündeki çipte gösterilir.
  final String ders;

  /// Kısa açıklama; önerme yanlışsa doğrusunu söyler.
  final String aciklama;

  const DogruYanlisOnerme({
    required this.metin,
    required this.dogru,
    required this.ders,
    required this.aciklama,
  });
}

